#include "cub3d.h"

t_surface	*t_surface_replace(t_surface *dest, t_surface *src)
{
	if (!dest || !src)
		return (dest);
	dest->width = src->width;
	dest->height = src->height;
	dest->data = src->data;
	dest->bpp = src->bpp;
	dest->line_length = src->line_length;
	dest->endian = src->endian;
	return (dest);
}
