#include "cub3d.h"

t_surface	*t_surface_fullcopy(t_surface *dest, t_surface *src)
{
	if (!dest || !src)
		return (dest);
	t_surface_copy(
		dest, src, (int []){0, 0, dest->width, dest->height},
		(int []){0, 0, src->width, src->height});
	return (dest);
}
