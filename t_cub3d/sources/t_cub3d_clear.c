#include "cub3d.h"

t_cub3d	*t_cub3d_clear(t_cub3d *cub)
{
	if (!cub || !cub->image || !cub->map)
		return (cub);
	t_surface_clear(cub->image, cub->map->floor_color);
	t_surface_setcolor(cub->image, cub->map->ceiling_color);
	t_surface_rect(cub->image, (int []){0, 0, cub->width, cub->height / 2}, 0);
	return (cub);
}
