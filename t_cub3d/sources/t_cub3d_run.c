#include "cub3d.h"

t_cub3d	*t_cub3d_run(t_cub3d *cub)
{
	if (!cub || !cub->mlx)
		return (cub);
	if (!t_cub3d_loading(cub))
		t_cub3d_update(cub);
	t_cub3d_draw(cub);
	mlx_loop(cub->mlx);
	return (cub);
}
