#include "cub3d.h"

t_cub3d	*t_cub3d_update(t_cub3d *cub)
{
	if (!cub || cub->pause)
		return (cub);
	if (t_cub3d_loading(cub))
		return (cub);
	t_cub3d_clear(cub);
	t_raycasting_update(cub->raycasting);
	return (cub);
}
