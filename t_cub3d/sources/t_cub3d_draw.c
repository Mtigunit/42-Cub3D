#include "cub3d.h"

t_cub3d	*t_cub3d_draw(t_cub3d *cub)
{
	if (!cub || cub->pause)
		return (cub);
	if (!t_cub3d_loading(cub))
	{
		t_map_draw(cub->map);
	}
	if (cub->mlx && cub->win && cub->img)
		mlx_put_image_to_window(cub->mlx, cub->win, cub->img, 0, 0);
	return (cub);
}
