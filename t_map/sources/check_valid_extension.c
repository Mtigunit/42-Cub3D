#include "cub3d.h"

void	check_valid_extension(char *path)
{
	if (!path)
		return (print_error("Invalid map\n"), exit(1));
	if (ft_memcmp(path + ft_strlen(path) - 4, ".cub", 4))
		return (print_error("Invalid file extension\n"), exit(1));
}
