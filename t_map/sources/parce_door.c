#include "cub3d.h"

void	parce_door(char **map, int x, int y, int map_len)
{
	if (!map || !*map)
		return (print_error("parce_zero: Invalid map.\n"), exit(1));
	if (x >= 0 && y >= 0 && y < map_len && (size_t)x < ft_strlen(map[y]))
	{
		if (((map[y + 1][x] != '1') || (map[y - 1][x] != '1'))
			&& ((map[y][x + 1] != '1') || (map[y][x - 1] != '1')))
			return (print_error("parce_zero: Invalid map.\n"), exit(1));
	}
}
