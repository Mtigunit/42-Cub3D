#include "cub3d.h"

static void	free_map(char **map)
{
	int	i;

	i = 0;
	while (map[i])
		free(map[i++]);
	free(map);
}

t_map	*ft_free_struct(t_map *p)
{
	if (!p)
		return (NULL);
	if (p->data)
		free_map(p->data);
	p->data = NULL;
	free(p->ea);
	p->ea = NULL;
	free(p->no);
	p->no = NULL;
	free(p->so);
	p->so = NULL;
	free(p->we);
	p->we = NULL;
	return (NULL);
}
