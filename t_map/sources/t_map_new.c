#include "cub3d.h"

t_map	*t_map_new(char *filename)
{
	static t_map	map;

	if (!filename)
		return ((t_map *)0);
	map.ceiling_color = 0x00bfd8f1;
	map.floor_color = 0x00BBBBBB;
	map.scale = 10;
	map.so = 0;
	map.no = 0;
	map.ea = 0;
	map.we = 0;
	t_map_init(&map, filename);
	map.ceiling_color = (map.c[0] << 16) + (map.c[1] << 8) + (map.c[2]);
	map.floor_color = (map.f[0] << 16) + (map.f[1] << 8) + (map.f[2]);
	return (&map);
}
