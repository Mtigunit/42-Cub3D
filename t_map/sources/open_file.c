#include "cub3d.h"

int	open_file(char *path)
{
	int	fd;

	fd = open(path, O_RDONLY);
	if (fd < 0)
	{
		print_error("Map: bad file descriptor\n");
		close(fd);
		exit(1);
	}
	return (fd);
}
