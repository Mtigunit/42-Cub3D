#include "cub3d.h"

void	texture_extension(const char *path)
{
	int	len;

	if (!path)
		return ;
	len = ft_strlen(path) - 4;
	if (ft_memcmp(path + len, ".xpm", 4))
	{
		print_error("Invalid texture\n");
		exit(1);
	}
}
