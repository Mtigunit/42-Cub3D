#include "cub3d.h"

int	check_wall_line(const char *str)
{
	int	i;
	int	flag;

	if (!str)
		return (FAILURE);
	i = 0;
	flag = 0;
	while (str[i])
	{
		if (str[i] == '1')
			flag++;
		if (str[i] != '1' && !is_white_spaces(str[i]))
			return (FAILURE);
		i++;
	}
	if (!flag)
		return (FAILURE);
	return (SUCCESS);
}
