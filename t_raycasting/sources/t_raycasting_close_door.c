#include "cub3d.h"

void	t_raycasting_close_door(t_map *map, int x, int y, float distance)
{
	if (map && (MIN_DOOR_DISTANCE + 0.2 < distance))
	{
		if (t_map_getelement(map, x, y) == 'O')
			t_map_setelement(map, x, y, 'C');
	}
}
